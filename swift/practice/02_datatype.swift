import Foundation

// Swift의 기본 데이터 타입
// Bool, Int, UInt, Float, Double, Character, String

// Bool
var someBool: Bool  = true
someBool = false
// someBool = 0
// someBool = 1

// Int
var someInt: Int = -100
// someInt = 100.1

// UInt
var someUInt: UInt = 100
// someUInt = someInt

// Float
var someFloat: Float = 3.14
someFloat = 3

//Double
var someDouble: Double = 3.14
someDouble = 3
//someDouble = someFloat

// Character
var someCharacter: Character = "s"
someCharacter = "1"
someCharacter = "가"
//someCharacter = "gkgkgk"
print(someCharacter)

// String
var someString: String = "하하하 __"
someString = someString + "웃으면 복이와요"
print(someString)
